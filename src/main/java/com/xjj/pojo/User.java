package com.xjj.pojo;

public class User {
	int uid;
	String uname;
	String pwd;
	public int getUid() {
		return uid;
	}
	public String getUname() {
		return uname;
	}
	public String getPwd() {
		return pwd;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
}
